package study.patterns.facade;

public class PopcornPopper {

    public void on()    {
        System.out.println("Popcorn popper is on");
    }

    public void off()   {
        System.out.println("Popcorn popper is off");
    }

    public void pop()   {
        System.out.println("Popcorn popper pop");
    }

}
