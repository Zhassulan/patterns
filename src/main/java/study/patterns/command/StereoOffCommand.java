package study.patterns.command;

import study.patterns.command.interfaces.ICommand;

public class StereoOffCommand implements ICommand {

    Stereo stereo;

    public StereoOffCommand(Stereo stereo) {
        this.stereo = stereo;
    }

    @Override
    public void execute() {
        stereo.off();
    }

    @Override
    public void undo() {
        stereo.on();
    }
}
